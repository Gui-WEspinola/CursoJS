// existem pré e pós para incremento e decremento
let contador = 1;
console.log(contador++);
console.log(contador);

let contador2 = 1;
console.log("Contador 2:", ++contador2);

console.log("Decremento:", --contador2);

const passo = 2;
contador += passo;
console.log(contador);

contador2 += 1;
contador2 **= 5;
console.log(contador2);

